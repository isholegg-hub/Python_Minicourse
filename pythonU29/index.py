# Як виміряти розмір папки чи файлу python?


from pathlib import Path

# Вказуємо шлях до файлу або папки
path = Path("testFolder")  

# Функція для обчислення розміру папки
def folder_size(path):
    total = 0
    for f in path.rglob('*'):  # rglob('*') шукає всі файли в папці та підпапках
        if f.is_file():         # перевіряємо, що це файл
            total += f.stat().st_size  # додаємо розмір файлу
    return total

# Отримуємо розмір папки в байтах
size_in_bytes = folder_size(path)
# Переводимо байти в мегабайти
size_in_mb = size_in_bytes / (1024*1024)

print(f"Розмір папки: {size_in_mb:.2f} МБ")



