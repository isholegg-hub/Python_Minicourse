# Як застосовувати map() в Python ефективно?



# --- map(): застосовуємо функцію до КОЖНОГО елемента списку ---

numbers = ["1", "2", "3"]

# Конвертація типів (рядки -> числа)
nums = list(map(int, numbers))
print(nums)   # → [1, 2, 3]

# Множення кожного елемента
doubled = list(map(lambda x: x * 2, nums))
print(doubled)  # → [2, 4, 6]


# map() дозволяє перетворювати кожен елемент списку БЕЗ циклів.
