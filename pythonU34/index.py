# Як працювати з базою даних SQLite в Python?


# Підключення до бази даних SQLite та створення таблиці

import sqlite3

# 1. Підключення до бази
# ':memory:' = тимчасова база в пам'яті
# 'my_first_db.db' = база у вигляді файлу на диску
conn = sqlite3.connect('my_first_db.db')  # створюємо або відкриваємо файл бази
c = conn.cursor()  # створюємо "курсор" для виконання команд

# 2. Створення таблиці users, якщо її ще немає
c.execute('''
    CREATE TABLE IF NOT EXISTS users (
        id INTEGER,
        name TEXT
    )
''')

# 3. Збереження змін у базі
conn.commit()

# 4. Закриття підключення
conn.close()

print("База підключена, таблиця 'users' створена або вже існувала.")

