# Як прочитати CSV без pandas?


import csv  # Імпортуємо модуль для роботи з CSV-файлами

# Відкриваємо CSV-файл у режимі читання з вказаним кодуванням UTF-8
with open("data.csv", newline='', encoding="utf-8") as csvfile:
    reader = csv.reader(csvfile)  # Створюємо об'єкт reader для читання рядків файлу
    header = next(reader)         # Читаємо перший рядок як заголовок
    print("Заголовки:", header)  # Виводимо заголовки на екран

    # Перебираємо всі рядки CSV-файлу після заголовка
    for row in reader:
        print("Рядок даних:", row)  # Виводимо кожен рядок даних на екран






