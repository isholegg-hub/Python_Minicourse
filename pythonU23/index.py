import re
print("---")

#  Знайти всі цифри в тексті
text2 = "В моєму номері 123456 та 78 є цифри"

pattern_digits = r"\d+"  # \d означає цифру, + — одна або більше

digits = re.findall(pattern_digits, text2)

print("Знайдені цифри:", digits)
