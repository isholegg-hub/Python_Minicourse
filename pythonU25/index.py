# 3 трюки з datetime на Python



from datetime import datetime, timedelta

# 1) Теперішній час (дуже часто потрібно)
now = datetime.now()
print("Зараз:", now.strftime("%Y-%m-%d %H:%M:%S"))

# 2) Різниця між датами (мега-корисно)
date1 = datetime(2025, 1, 1)
date2 = datetime(2025, 2, 15)
diff = date2 - date1
print("Різниця в днях:", diff.days)

# # 3) Форматування дати (тут трапляються найбільші помилки)
pretty = now.strftime("%A, %d %B %Y")
print("Гарний формат:", pretty)



