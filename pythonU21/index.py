# Найзручніший цикл у Python з enumerate() ?



items = ["молоко", "хліб", "печиво"]

# enumerate дає (індекс, значення)
for i, value in enumerate(items, start=1):
    print(i, value)
# Коментар: start=1 → почати нумерацію не з нуля, а з одиниці


